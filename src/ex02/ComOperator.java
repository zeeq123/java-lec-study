package ex02;

public class ComOperator {
    public static void main(String[] args) {
        System.out.println((3 == 4) + " ");
        System.out.println((3 != 4) + " ");
        System.out.println((3 > 4) + " ");
        System.out.println((4 > 3) + " ");

        System.out.println((3 == 3 && 4 == 7) + " ");
        System.out.println((3 == 3 || 4 == 7) + " ");
    }
}
